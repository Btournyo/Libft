#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = 0;
	if (n == 2147483647)
	{
		ft_putchar('2');
		ft_putnbr(147483647);
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n & 10);
	}
	else
		ft_putchar(n + 48);
}
